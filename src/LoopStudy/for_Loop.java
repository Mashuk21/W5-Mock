package LoopStudy;

public class for_Loop {

	public static void main(String[] args) {

		for(int a=1;a<=10;a++)
		{
			System.out.println(a);
		}
		
		for(int b=10;b>=1;b--)
		{
			System.out.println(b);
		}
		
		for(int c=2;c<=20;c=c+2)
		{
			System.out.println(c);
		}
		for (int d=30;d>=3;d=d-3)
		{
			System.out.println(d);
		}
	}
	
}
