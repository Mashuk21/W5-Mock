package LoopStudy;

public class while_Loop {

	public static void main(String[] args) {
		
		int a=1;
		while(a<=10)
		{
		System.out.println(a);
		a++;
		}
		
		int b=2;
		while(b<=20)
		{
			System.out.println(b);
			b=b+2;
		}
		
		int c=30;
		while(c>=3)
		{
			System.out.println(c);
			c=c-3;
		}
		
		
		
		
		

	}

}
